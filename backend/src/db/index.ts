      'r2_mount_points',
      'onedrive_auth',
      'onedrive_mount_points',
      'webdav_mount_points',
      'email_verification_codes',
      'smtp_config',
      'download_tokens',
      'file_direct_links',
      'direct_link_access_logs',
      'ip_bans',
      'file_shares',
